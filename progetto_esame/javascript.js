//funzione per rendere visibili le informazioni sulla cartolina 124
function scopri124() {
  var info124 = document.getElementById("scopri124");
  if (info124.style.display === "none") {
    info124.style.display = "block";
  } else {
    info124.style.display = "none";
  }
};
